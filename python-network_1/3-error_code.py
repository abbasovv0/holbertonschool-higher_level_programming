#!/usr/bin/python3
"""DOCUMENTED."""

import sys
import urllib.request
import urllib.error


def main():
    url = sys.argv[1]

    try:
        with urllib.request.urlopen(url) as response:
            body = response.read().decode('utf-8')
            print(body)
    except urllib.error.HTTPError as e:
        print(f"Error code: {e.code}")


if __name__ == "__main__":
    main()
